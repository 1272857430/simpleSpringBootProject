package cn.cjd.springboot.modal.common.utils.dataTimeUtils;

public enum DatePart
{
    /**
     * 表示年份部分。
     */
    year,

    /**
     * 表示月份部分。
     */
    month,

    /**
     * 表示该月中的第几天。
     */
    day,

    /**
     * 表示小时部分。
     */
    hour,

    /**
     * 表示分钟部分。
     */
    minute,

    /**
     * 表示秒部分。
     */
    second,
    
    /**
     * 表示毫秒部分。
     */
    millisecond;
}
